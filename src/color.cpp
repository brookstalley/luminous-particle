cieXYcolor cieXYfromWavelength(uint16_t wavelength) {
  cieXYcolor xy(wavelengthToXY[wavelength - 360][0], wavelengthToXY[wavelength - 360][1]);

  return xy;
}

cieUVcolor cieUVfromWavelength(uint16_t wavelength) {
  return cieUVfromXY(cieXYfromWavelength(wavelength));
}

cieUVcolor cieUVfromXY(const cieXYcolor xyColor) {
  cieUVcolor uv((4 * xyColor.x) / (12 * xyColor.y - 2 * xyColor.x + 3),
                (9 * xyColor.y) / (12 * xyColor.y - 2 * xyColor.x + 3));

  return uv;
}

